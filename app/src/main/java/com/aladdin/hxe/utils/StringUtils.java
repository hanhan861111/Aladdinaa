	//判断日期格式是否有效
	public static boolean isDateStringValid(String date) {
		SimpleDateFormat sdf=new SimpleDateFormat("yyyy/MM/dd");
		Date d=new Date();
		try {
			d = sdf.parse(date);
		} catch (Exception e) {
		}
		String result=sdf.format(d);
		return result.equals(date);
	}
